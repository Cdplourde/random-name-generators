<h2>About Us</h2>
<p>Hi there, and thanks for visiting! Random Name Generators is a tool designed to help you find the perfect name. We’ve scoured the web to curate the ultimate list of options and built them into user-friendly generators.</p>
<p>There are tens of thousands of names out there, and more are invented every day. With so many options, it can be hard to decide. With our tool, you can consider name options in smaller, more manageable chunks.</p>
<p>If you see something that catches your eye, jot it down! If not, just one click is all it takes to build a new array of choices.</p>
<h3>What can these generators be used for?</h3>
<p>Brainstorm names for any purpose! Babies, pets, novel characters, roleplay characters — even new names for yourself.</p>
<h3>How many names are included in the tool?</h3>
<p>Currently, our tool consists of about 1800 first names and over 2000 last names. We occasionally update to add more names as we discover them. </p>
<h3>What generators will be added in the future?</h3>
<p>Random Name Generators is a side project, and it takes time to build new generators. We have plans to add more, but we can’t promise any dates. Our goal is to branch into a variety of fun, goofy, and fantasy generators one day.</p>
